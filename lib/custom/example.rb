# An example Ruby class.
class Example
  # Adds to integers together.
  # @param [Integer] x The first integer.
  # @param [Integer] y The second integer.
  # @return [Integer] Returns the sum of x and y.
  # @example Add two integers together.
  #   Example.add(1, 2) => 3
  def self.add(x, y)
    x + y
  end
end
